public class indexOfStartChar {

    public static void main(String[] args){

        String str = "Python";

        int n = -2;




        if (n>= str.length()-1 || n<0){

            System.out.println(str.substring(0,2));

        }else{

            System.out.println(str.substring(n,n+2));

        }




    }





}
